CONCEPT_MERGE_SYSTEM = (
    "你是课程概念词条库的维护者。你会拿到一个【已有概念词条的完整 Markdown】，"
    "以及【来自某一节新课的补充材料】。请把两者整合为一篇更完整、统一的词条。\n\n"
    "要求：\n"
    "1. 保留已有词条中所有准确的信息，把新材料中的新角度、新细节自然地融合进对应小节，"
    "不要简单堆在文末。\n"
    "2.【绝不重复】不要出现重复的句子或论述；同一要点只保留一处最完整的表述。\n"
    "3.【绝不编造】只整合两份输入里出现的内容，不得新增凭空的史实、年代或因果；拿不准的不写。\n"
    "4. 维护词条结构（定义 / 详细解释 / 在课程中的体现 / 关联概念），"
    "并在“涉及课时”里补上这节新课的课时号（避免重复添加）。\n"
    "5. 简体中文。输出整篇整合后的 Markdown 词条，不要任何额外说明。"
)


def build_concept_merge_user(title: str, existing_md: str, lecture_id: str, concept_obj: dict) -> str:
    supp = (concept_obj.get("explanation") or "").strip()
    summ = (concept_obj.get("summary") or "").strip()
    return (
        f"概念名称：{title}\n新课时：{lecture_id}\n\n"
        "【已有词条 Markdown】\n"
        "----- 开始 -----\n"
        f"{existing_md}\n"
        "----- 结束 -----\n\n"
        f"【本节课的补充材料】\n一句话定义（参考）：{summ}\n补充解释：\n{supp}\n"
    )


def build_new_concept_file(course: str, title: str, concept_obj: dict, lecture_id: str) -> str:
    """根据抽取结果，用模板生成全新概念词条（不再额外调用 API）。"""
    aliases = concept_obj.get("aliases") or []
    alias_line = "、".join(aliases) if aliases else "无"
    summary = (concept_obj.get("summary") or "").strip()
    explanation = (concept_obj.get("explanation") or "").strip()
    fm = (
        "---\n"
        f"title: {title}\n"
        f"course: {course}\n"
        "type: concept\n"
        f"aliases: [{', '.join(aliases)}]\n"
        f"lectures: [{lecture_id}]\n"
        "---\n\n"
    )
    body = (
        f"# {title}\n\n"
        f"> 别名：{alias_line}\n\n"
        "## 定义\n\n"
        f"{summary}\n\n"
        "## 详细解释\n\n"
        f"{explanation}\n\n"
        "## 在课程中的体现\n\n"
        f"- {lecture_id}：本节课中出现并讲解。\n\n"
        "## 关联概念\n\n"
        "（在主题索引中建立关联）\n"
    )
    return fm + body


